async function getDocumentData(db: D1Database, companyId: string, docType: string, docId: string) {
  let doc: any = null;
  let lines: any[] = [];
  let customerName = '';

  if (docType === 'invoice') {
    doc = await db.prepare('SELECT * FROM invoices WHERE id = ? AND company_id = ?').bind(docId, companyId).first();
    if (doc) {
      const lineResult = await db.prepare('SELECT * FROM invoice_items WHERE invoice_id = ?').bind(docId).all();
      lines = (lineResult.results || []) as any[];
      const cust = await db.prepare('SELECT customer_name FROM customers WHERE id = ?').bind(doc.customer_id).first();
      customerName = (cust as any)?.customer_name || 'Unknown';
    }
  } else if (docType === 'quote') {
    doc = await db.prepare('SELECT * FROM quotes WHERE id = ? AND company_id = ?').bind(docId, companyId).first();
    if (doc) {
      const lineResult = await db.prepare('SELECT * FROM quote_items WHERE quote_id = ?').bind(docId).all();
      lines = (lineResult.results || []) as any[];
      const cust = await db.prepare('SELECT customer_name FROM customers WHERE id = ?').bind(doc.customer_id).first();
      customerName = (cust as any)?.customer_name || 'Unknown';
    }
  } else if (docType === 'sales_order' || docType === 'sales-order') {
    doc = await db.prepare('SELECT * FROM sales_orders WHERE id = ? AND company_id = ?').bind(docId, companyId).first();
    if (doc) {
      const lineResult = await db.prepare('SELECT * FROM sales_order_items WHERE sales_order_id = ?').bind(docId).all();
      lines = (lineResult.results || []) as any[];
      const cust = await db.prepare('SELECT customer_name FROM customers WHERE id = ?').bind(doc.customer_id).first();
      customerName = (cust as any)?.customer_name || 'Unknown';
    }
  } else if (docType === 'purchase_order' || docType === 'purchase-order') {
    doc = await db.prepare('SELECT * FROM purchase_orders WHERE id = ? AND company_id = ?').bind(docId, companyId).first();
    if (doc) {
      const lineResult = await db.prepare('SELECT * FROM purchase_order_items WHERE purchase_order_id = ?').bind(docId).all();
      lines = (lineResult.results || []) as any[];
      const sup = await db.prepare('SELECT supplier_name FROM suppliers WHERE id = ?').bind(doc.supplier_id).first();
      customerName = (sup as any)?.supplier_name || 'Unknown';
    }
  } else if (docType === 'credit_note' || docType === 'credit-note') {
    doc = await db.prepare('SELECT * FROM invoices WHERE id = ? AND company_id = ? AND invoice_type = ?').bind(docId, companyId, 'credit_note').first();
    if (!doc) {
      doc = await db.prepare('SELECT * FROM invoices WHERE id = ? AND company_id = ?').bind(docId, companyId).first();
    }
    if (doc) {
      const lineResult = await db.prepare('SELECT * FROM invoice_items WHERE invoice_id = ?').bind(docId).all();
      lines = (lineResult.results || []) as any[];
      const cust = await db.prepare('SELECT customer_name FROM customers WHERE id = ?').bind(doc.customer_id).first();
      customerName = (cust as any)?.customer_name || 'Unknown';
    }
  }

  return { doc, lines, customerName };
}

// Generate PDF (HTML) for any document type
app.get('/pdf/:docType/:docId', async (c) => {
  try {
    const companyId = await getSecureCompanyId(c);
    const docType = c.req.param('docType');
    const docId = c.req.param('docId');
    
    const { doc, lines, customerName } = await getDocumentData(c.env.DB, companyId, docType, docId);
    
    if (!doc) {
      return c.json({ error: 'Document not found' }, 404);
    }

    const total = Number(doc.total_amount || 0);
    const vat = total * 0.15 / 1.15;
    const subtotal = total - vat;
    const docNumber = doc.invoice_number || doc.quote_number || doc.order_number || doc.po_number || docId;
    const docDate = doc.invoice_date || doc.quote_date || doc.order_date || doc.created_at || new Date().toISOString();
    const currency = doc.currency || 'ZAR';

    const titleMap: Record<string, string> = {
      invoice: 'Tax Invoice',
      quote: 'Quotation',
      sales_order: 'Sales Order',
      'sales-order': 'Sales Order',
      purchase_order: 'Purchase Order',
      'purchase-order': 'Purchase Order',
      credit_note: 'Credit Note',
      'credit-note': 'Credit Note',
      statement: 'Customer Statement',
    };

    const company = await c.env.DB.prepare('SELECT company_name FROM companies WHERE id = ?').bind(companyId).first();
    const companyName = (company as any)?.company_name || 'ARIA ERP';

    const html = generatePdfHtml(
      titleMap[docType] || 'Document',
      docNumber,
      docDate.split('T')[0],
      customerName,
      lines,
      { subtotal, vat, total },
      currency,
      companyName,
      { Status: doc.status || 'draft' }
    );

    return new Response(html, {
      headers: {
        'Content-Type': 'text/html; charset=utf-8',
        'Content-Disposition': `inline; filename="${docType}-${docNumber}.html"`,
      },
    });
  } catch (error) {
    return c.json({ error: String(error) }, 500);
  }
});

