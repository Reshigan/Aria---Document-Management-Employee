app.post('/migration/import', async (c) => {
  try {
    const companyId = await getSecureCompanyId(c);
    const body = await c.req.json();
    const { entity, data } = body;

    if (!entity || !Array.isArray(data)) {
      return c.json({ error: 'entity (string) and data (array) are required' }, 400);
    }

    const validEntities = ['customers', 'suppliers', 'products', 'invoices', 'employees'];
    if (!validEntities.includes(entity)) {
      return c.json({ error: `Invalid entity. Valid: ${validEntities.join(', ')}` }, 400);
    }

    let imported = 0;
    let errors: string[] = [];

    for (const record of data) {
      try {
        const id = record.id || crypto.randomUUID();
        if (entity === 'customers') {
          await c.env.DB.prepare(
            'INSERT OR IGNORE INTO customers (id, company_id, customer_name, email, phone, address, created_at) VALUES (?, ?, ?, ?, ?, ?, ?)'
          ).bind(id, companyId, record.customer_name || record.name, record.email, record.phone, record.address, new Date().toISOString()).run();
        } else if (entity === 'suppliers') {
          await c.env.DB.prepare(
            'INSERT OR IGNORE INTO suppliers (id, company_id, supplier_name, email, phone, address, created_at) VALUES (?, ?, ?, ?, ?, ?, ?)'
          ).bind(id, companyId, record.supplier_name || record.name, record.email, record.phone, record.address, new Date().toISOString()).run();
        } else if (entity === 'products') {
          await c.env.DB.prepare(
            'INSERT OR IGNORE INTO products (id, company_id, product_name, sku, unit_price, cost_price, quantity_on_hand, created_at) VALUES (?, ?, ?, ?, ?, ?, ?, ?)'
          ).bind(id, companyId, record.product_name || record.name, record.sku, Number(record.unit_price || 0), Number(record.cost_price || 0), Number(record.quantity_on_hand || 0), new Date().toISOString()).run();
        }
        imported++;
      } catch (e) {
        errors.push(`Row ${imported + errors.length + 1}: ${String(e)}`);
      }
    }

    return c.json({
      success: true,
      imported,
      errors: errors.length,
      error_details: errors.slice(0, 10),
      message: `Imported ${imported} ${entity} records`,
    });
  } catch (error) {
    return c.json({ error: String(error) }, 500);
  }
});

