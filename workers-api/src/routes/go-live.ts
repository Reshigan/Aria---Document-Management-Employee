function toCsv(rows: any[], columns?: string[]): string {
  if (!rows.length) return '';
  const cols = columns || Object.keys(rows[0]);
  const header = cols.join(',');
  const body = rows.map(row => cols.map(col => {
    const val = row[col];
    if (val === null || val === undefined) return '';
    const str = String(val);
    return str.includes(',') || str.includes('"') || str.includes('\n') ? `"${str.replace(/"/g, '""')}"` : str;
  }).join(',')).join('\n');
  return `${header}\n${body}`;
}

app.get('/export/:entity', async (c) => {
  try {
    const companyId = await getSecureCompanyId(c);
    const entity = c.req.param('entity');
    const format = c.req.query('format') || 'csv';
    
    const tableMap: Record<string, { table: string; orderBy: string }> = {
      customers: { table: 'customers', orderBy: 'customer_name' },
      suppliers: { table: 'suppliers', orderBy: 'supplier_name' },
      products: { table: 'products', orderBy: 'product_name' },
      invoices: { table: 'invoices', orderBy: 'created_at DESC' },
      quotes: { table: 'quotes', orderBy: 'created_at DESC' },
      'sales-orders': { table: 'sales_orders', orderBy: 'created_at DESC' },
      'purchase-orders': { table: 'purchase_orders', orderBy: 'created_at DESC' },
      employees: { table: 'employees', orderBy: 'full_name' },
      'journal-entries': { table: 'gl_journal_entries', orderBy: 'entry_date DESC' },
      'stock-movements': { table: 'stock_movements', orderBy: 'created_at DESC' },
    };

    const config = tableMap[entity];
    if (!config) {
      return c.json({ error: `Unknown entity: ${entity}. Valid: ${Object.keys(tableMap).join(', ')}` }, 400);
    }

    const result = await c.env.DB.prepare(
      `SELECT * FROM ${config.table} WHERE company_id = ? ORDER BY ${config.orderBy} LIMIT 10000`
    ).bind(companyId).all();

    const rows = (result.results || []) as any[];
    
    if (format === 'json') {
      return c.json({ success: true, data: rows, count: rows.length });
    }

    const csv = toCsv(rows);
    return new Response(csv, {
      headers: {
        'Content-Type': 'text/csv; charset=utf-8',
        'Content-Disposition': `attachment; filename="${entity}-export-${Date.now()}.csv"`,
      },
    });
  } catch (error) {
    return c.json({ error: String(error) }, 500);
  }
});

