app.post('/currency/convert-invoice', async (c) => {
  try {
    const companyId = await getSecureCompanyId(c);
    const body = await c.req.json();
    const { invoice_id, target_currency } = body;

    const invoice = await c.env.DB.prepare('SELECT * FROM invoices WHERE id = ? AND company_id = ?').bind(invoice_id, companyId).first();
    if (!invoice) {
      return c.json({ error: 'Invoice not found' }, 404);
    }

    const sourceCurrency = (invoice as any).currency || 'ZAR';
    const amount = Number((invoice as any).total_amount || 0);

    const rateResult = await c.env.DB.prepare(
      'SELECT rate FROM exchange_rates WHERE company_id = ? AND from_currency = ? AND to_currency = ? ORDER BY effective_date DESC LIMIT 1'
    ).bind(companyId, sourceCurrency, target_currency).first();

    const rate = rateResult ? Number((rateResult as any).rate) : (target_currency === 'USD' ? 0.055 : target_currency === 'EUR' ? 0.051 : target_currency === 'GBP' ? 0.044 : 1);
    const convertedAmount = amount * rate;

    return c.json({
      success: true,
      data: {
        original_amount: amount,
        original_currency: sourceCurrency,
        converted_amount: Math.round(convertedAmount * 100) / 100,
        target_currency,
        exchange_rate: rate,
        conversion_date: new Date().toISOString(),
      },
    });
  } catch (error) {
    return c.json({ error: String(error) }, 500);
  }
});

