app.get('/audit-trail', async (c) => {
  try {
    const companyId = await getSecureCompanyId(c);
    const page = parseInt(c.req.query('page') || '1');
    const pageSize = parseInt(c.req.query('page_size') || '50');
    const eventType = c.req.query('event_type');
    const action = c.req.query('action');
    const userId = c.req.query('user_id');
    const startDate = c.req.query('start_date');
    const endDate = c.req.query('end_date');
    const resourceType = c.req.query('resource_type');

    let query = `SELECT al.*, u.full_name as user_name, u.email as user_email
      FROM audit_logs al LEFT JOIN users u ON al.user_id = u.id
      WHERE al.company_id = ?`;
    const params: any[] = [companyId];

    if (eventType) { query += ' AND al.event_type = ?'; params.push(eventType); }
    if (action) { query += ' AND al.action LIKE ?'; params.push(`%${action}%`); }
    if (userId) { query += ' AND al.user_id = ?'; params.push(userId); }
    if (startDate) { query += ' AND al.created_at >= ?'; params.push(startDate); }
    if (endDate) { query += ' AND al.created_at <= ?'; params.push(endDate); }
    if (resourceType) { query += ' AND al.resource_type = ?'; params.push(resourceType); }

    const countQuery = query.replace(/SELECT al\.\*, u\.full_name as user_name, u\.email as user_email/, 'SELECT COUNT(*) as total');
    const countResult = await c.env.DB.prepare(countQuery).bind(...params).first();
    const total = (countResult as any)?.total || 0;

    query += ` ORDER BY al.created_at DESC LIMIT ? OFFSET ?`;
    params.push(pageSize, (page - 1) * pageSize);

    const result = await c.env.DB.prepare(query).bind(...params).all();

    return c.json({
      success: true,
      data: result.results || [],
      pagination: { page, page_size: pageSize, total, total_pages: Math.ceil(total / pageSize) },
    });
  } catch (error) {
    return c.json({ success: true, data: [], pagination: { page: 1, page_size: 50, total: 0, total_pages: 0 } });
  }
});

