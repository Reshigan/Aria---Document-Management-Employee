let schemaHealed = false;

export async function healSchema(db: D1Database): Promise<{ tablesCreated: number; columnsAdded: number }> {
  if (schemaHealed) return { tablesCreated: 0, columnsAdded: 0 };

  let tablesCreated = 0;
  let columnsAdded = 0;

  const existingTables = new Set<string>();
  try {
    const result = await db.prepare(
      "SELECT name FROM sqlite_master WHERE type='table'"
    ).all<{ name: string }>();
    for (const row of result.results || []) {
      existingTables.add(row.name);
    }
  } catch {
    return { tablesCreated: 0, columnsAdded: 0 };
  }

  for (const [tableName, columns] of Object.entries(SCHEMA)) {
    if (!existingTables.has(tableName)) {
      const colDefs = columns.map(c => `${c.name} ${c.definition}`).join(', ');
      try {
        await db.prepare(`CREATE TABLE IF NOT EXISTS ${tableName} (${colDefs})`).run();
        tablesCreated++;
      } catch (e) {
        console.error(`[schema-healer] Failed to create table ${tableName}:`, e);
      }
      continue;
    }

    let existingColumns: Set<string>;
    try {
      const info = await db.prepare(`PRAGMA table_info(${tableName})`).all<{ name: string }>();
      existingColumns = new Set((info.results || []).map(r => r.name));
    } catch {
      continue;
    }

    for (const col of columns) {
      if (!existingColumns.has(col.name)) {
        try {
          await db.prepare(`ALTER TABLE ${tableName} ADD COLUMN ${col.name} ${col.definition}`).run();
          columnsAdded++;
        } catch (e) {
          const msg = String(e);
          if (!msg.includes('duplicate column')) {
            console.error(`[schema-healer] Failed to add ${tableName}.${col.name}:`, e);
          }
        }
      }
    }
  }

  schemaHealed = true;
  console.log(`[schema-healer] Done: ${tablesCreated} tables created, ${columnsAdded} columns added`);
  return { tablesCreated, columnsAdded };
}

export function resetHealerCache(): void {
  schemaHealed = false;
}
